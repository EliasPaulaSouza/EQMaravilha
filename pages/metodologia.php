          <h3>Criação da HQ: </h3>

          <li>
            <p> O modelo escolhido para produção da HQ foi proposto por Garone,
              Gallo e Campos (2016), para definir a história, apresentando então as
              sequenciais etapas: briefing, análise, roteiro, storyboard, diagramação,
              ilustração e avaliação. Porém neste projeto houve a necessidade de inverter
              a ordem de algumas fases, e foi adaptado da seguinte maneira: briefing, análise,
              roteiro, storyboard, diagramação, avaliação e ilustração.
            </p>
          <li> Com os estudos sobre HQ e a pesquisa realizada, foi produzido um briefing, agrupando todas as
            informações de enredo. Após todo o planejamento de personalidade e história, foi esboçado em
            folha de papel todo o roteiro proposto pelos autores. </li><br>
          <li> Em seguida, foi feita a etapa do layout (Storyboard e diagramação) com as páginas, quadros,
            balonamento, e esboços das cenas, em papel sulfite. Passando por uma avaliação da equipe,
            para conferir os textos e as imagens, finalizando com a etapa da ilustração, onde foi produzida a
            arte final por meio do software Adobe Photoshop, efetuando a arte de todas as páginas, incluindo a capa.
            Foi desenhada no PS utilizando uma mesa digitalizadora, usufruindo de diversos brushes (ferramentas “pincéis”
            dentro do software) para a criação de elementos constituintes nas páginas. Voltado ao texto e diálogos, uma
            fonte mais divertida foi escolhida, pois junto com a linguagem informal, remete ao cotidiano dos estudantes.</li>
          <br>
          </li>
          <br>
          <h3>Desenvolvimento do site: </h3>

          <li> Com a arte finalizada, construímos um meio para a divulgação da HQ, um website.
            Inicialmente, fez o levantamento dos requisitos que o site deveria atender e utilizou-se
            o diagrama de caso de uso para ilustrar quem seriam os atores e quais suas atividades dentro do sistema.
          </li><br>
          <li>Definindo as funcionalidades que o sistema deveria disponibilizar pensou-se no diagrama de classe que poderia ser
            utilizado para construção do site. Após isso, pensou-se no modelo do banco de dados que seria necessário para acomodar
            as informações. Por fim, para o front-end foi utilizado um template gratuito denominado Nocoboot,
            disponível em: <a href="https://www.free-css.com/free-css-templates/page267/nocobot.">
              https://www.free-css.com/free-css-templates/page267/nocobot.</a> Este template forneceu
