            com o conhecimento adquirido no curso técnico de informática do IFMS campus Jardim, foi possível adaptar
            e criar as páginas conforme os requisitos levantados.
          </li><br>
          <li>Para o back-end elegeu-se a linguagem PHP e servidor apache para fazer a
            verificação de login, cadastro/aceite/exclusão de comentário e a linguagem SQL
